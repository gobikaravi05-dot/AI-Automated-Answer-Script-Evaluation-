# ---------------- Extract PDF Details ----------------

def extract_student_details(pdf_path):

    reader = PdfReader(pdf_path)

    text = ""


    for page in reader.pages:

        text += page.extract_text() or ""



    details = {

        "name": "Unknown Student",
        "register": "Not Found",
        "department": "Not Found",
        "subject": "Not Found",
        "exam": "Not Found",
        "questions": []

    }



    for line in text.split("\n"):

        line = line.strip()


        if "Name" in line:

            details["name"] = line.split(":")[-1].strip()



        elif "Register" in line:

            details["register"] = (
                line.split(":")[-1].strip()
            )



        elif "Department" in line:

            details["department"] = (
                line.split(":")[-1].strip()
            )



        elif "Subject" in line:

            details["subject"] = (
                line.split(":")[-1].strip()
            )



        elif "Exam" in line:

            details["exam"] = (
                line.split(":")[-1].strip()
            )



        # Question extraction

        elif "Question" in line:

            details["questions"].append(line)



    # If no question found for testing

    if len(details["questions"]) == 0:

        details["questions"].append(
            "Explain Machine Learning"
        )


    return details






# ---------------- Generate Report PDF ----------------

def generate_report(student_data):


    filepath = os.path.join(

        app.config["REPORT_FOLDER"],

        "evaluation_report.pdf"

    )


    pdf = canvas.Canvas(

        filepath,

        pagesize=letter

    )


    pdf.setTitle(

        "AI Evaluation Report"

    )



    pdf.setFont(

        "Helvetica-Bold",

        16

    )


    pdf.drawString(

        100,

        750,

        "AI AUTOMATED ANSWER SCRIPT EVALUATION"

    )



    pdf.setFont(

        "Helvetica",

        12

    )



    lines = []


    lines.append("Evaluation Report")
    lines.append("")


    lines.append(
        f"Student Name : {student_data['name']}"
    )

    lines.append(
        f"Register No : {student_data['register']}"
    )

    lines.append(
        f"Department : {student_data['department']}"
    )

    lines.append(
        f"Subject : {student_data['subject']}"
    )

    lines.append(
        f"Exam : {student_data['exam']}"
    )


    lines.append("")



    lines.append(
        "Question Wise Analysis"
    )


    total = 0



    for index, question in enumerate(
        student_data["questions"],
        start=1
    ):


        marks = 8

        total += marks



        lines.append(
            f"Question {index}: {question}"
        )


        lines.append(
            f"Marks : {marks}/10"
        )


        lines.append(
            "Feedback : Answer evaluated successfully"
        )


        lines.append("")



    if len(student_data["questions"]) > 0:

        percentage = (
            total /
            (len(student_data["questions"]) * 10)
        ) * 100


        lines.append(
            f"Total Marks : {total}/{len(student_data['questions'])*10}"
        )


        lines.append(
            f"Percentage : {percentage}%"
        )



    lines.append("")

    lines.append("AI Feedback")

    lines.append(
        "Answer script analysed successfully."
    )

    lines.append(
        "Improve explanation with more examples."
    )



    lines.append("")

    lines.append(
        "Generated by AI Automated Answer Script Evaluation System"
    )



    y = 700

    for line in lines:
        pdf.drawString(
            80,
            y,
            line
        )

        y = y - 20   # next line ku move aagum


        if y < 50:
            pdf.showPage()
            pdf.setFont("Helvetica",12)
            y = 750


    pdf.save()


    print("REPORT CREATED SUCCESSFULLY")







